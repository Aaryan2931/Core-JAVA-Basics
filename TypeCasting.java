import java.util.*;

public class TypeCasting {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        float a = 25.12f;
        int b = (int) a;
        System.out.println(b);

        char ch = 'A'; // conversion of char value
        int num = (int) ch;
        System.out.println(num);

    }
}
