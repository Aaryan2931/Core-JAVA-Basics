import java.util.*;

public class Areaofsquare {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        System.out.print("Enter side of a Sqaure : ");
        int s = sc.nextInt();

        int area = s * s;
        System.out.print("Area of a Sqaure : ");
        System.out.print(area);

    }
}
